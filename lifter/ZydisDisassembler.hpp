// helper functions
inline OperandType zydisTypeToMergenType(ZydisOperandType type, uint8_t size,
                                         bool second_imm) {
  switch (type) {
  case ZYDIS_OPERAND_TYPE_IMMEDIATE:
    switch (size) {

    case 8:
      if (second_imm)
        return OperandType::Immediate8_2nd;
      return OperandType::Immediate8;
    case 16:
      return OperandType::Immediate16;
    case 32:
      return OperandType::Immediate32;
    case 64:
      return OperandType::Immediate64;
    }

  case ZYDIS_OPERAND_TYPE_REGISTER: {
    switch (size) {
    case 8:
      return OperandType::Register8;
    case 16:
      return OperandType::Register16;
    case 32:
      return OperandType::Register32;
    case 64:
      return OperandType::Register64;
    }
  }

  case ZYDIS_OPERAND_TYPE_MEMORY:

    switch (size) {

    case 8:
      return OperandType::Memory8;
    case 16:
      return OperandType::Memory16;
    case 32:
      return OperandType::Memory32;
    case 64:
      return OperandType::Memory64;
    }

  default:
    return OperandType::Invalid;
  }
}

